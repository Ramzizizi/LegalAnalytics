import pytest
from decimal import Decimal
from django.core.exceptions import ValidationError
from apps.catalog.validators import валидатор_инн, валидатор_сумма_положительная


# --- ИНН юрлица (10 цифр) ---
# Значения верифицированы вычислением контрольной цифры

ВАЛИДНЫЕ_ИНН_10 = [
    '7707083893',   # Сбербанк
    '7736207543',   # Газпром
    '3664069397',   # верифицирован алгоритмом
    '0000000000',   # нули математически проходят алгоритм (0%11%10=0)
]

НЕВАЛИДНЫЕ_ИНН_10 = [
    '7707083890',   # неверная контрольная цифра (должна быть 3)
    '7707083891',
    '7707083892',
    '1234567890',   # неверная контрольная цифра
]


# --- ИНН физлица/ИП (12 цифр) ---
# Сгенерированы программно из верифицированных 10-значных

ВАЛИДНЫЕ_ИНН_12 = [
    '770708389324',  # сгенерирован из 7707083893
    '773620754381',  # сгенерирован из 7736207543
]

НЕВАЛИДНЫЕ_ИНН_12 = [
    '770708389325',  # неверная 12-я цифра (должна быть 4)
    '770708389314',  # неверная 11-я цифра (должна быть 2)
    '773620754382',  # неверная 12-я цифра
]


# --- Форматные ошибки ---

НЕКОРРЕКТНЫЙ_ФОРМАТ = [
    '',               # пусто
    '123',            # слишком короткий
    '12345678901234', # слишком длинный
    '11111111111',    # 11 цифр (не 10 и не 12)
    'abcdefghij',     # буквы вместо цифр
    '770708389 ',     # пробел внутри
    '770.083893',     # точка внутри
]


@pytest.mark.parametrize('инн', ВАЛИДНЫЕ_ИНН_10)
def test_инн_юрлица_валидный(инн):
    валидатор_инн(инн)


@pytest.mark.parametrize('инн', ВАЛИДНЫЕ_ИНН_12)
def test_инн_физлица_валидный(инн):
    валидатор_инн(инн)


@pytest.mark.parametrize('инн', НЕВАЛИДНЫЕ_ИНН_10)
def test_инн_юрлица_невалидный(инн):
    with pytest.raises(ValidationError):
        валидатор_инн(инн)


@pytest.mark.parametrize('инн', НЕВАЛИДНЫЕ_ИНН_12)
def test_инн_физлица_невалидный(инн):
    with pytest.raises(ValidationError):
        валидатор_инн(инн)


@pytest.mark.parametrize('инн', НЕКОРРЕКТНЫЙ_ФОРМАТ)
def test_инн_некорректный_формат(инн):
    with pytest.raises(ValidationError):
        валидатор_инн(инн)


@pytest.mark.parametrize('сумма', [Decimal('0.01'), Decimal('1'), Decimal('999999.99')])
def test_сумма_положительная_валидна(сумма):
    валидатор_сумма_положительная(сумма)


@pytest.mark.parametrize('сумма', [Decimal('0'), Decimal('-1'), Decimal('-0.01')])
def test_сумма_неположительная_невалидна(сумма):
    with pytest.raises(ValidationError):
        валидатор_сумма_положительная(сумма)


def test_сумма_none_не_вызывает_ошибку():
    валидатор_сумма_положительная(None)
